package com.WattanArt;

public class PatternProperties {
    int position;
    int patterntyp;
    int patternAmount;
    double patternPrice;

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }

    public int getPatterntyp() {
        return patterntyp;
    }

    public void setPatterntyp(int patterntyp) {
        this.patterntyp = patterntyp;
    }

    public int getPatternAmount() {
        return patternAmount;
    }

    public void setPatternAmount(int patternAmount) {
        this.patternAmount = patternAmount;
    }

    public double getPatternPrice() {
        return patternPrice;
    }

    public void setPatternPrice(double patternPrice) {
        this.patternPrice = patternPrice;
    }
}
